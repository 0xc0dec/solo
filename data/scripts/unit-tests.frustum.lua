runTest(function()
	local f = solo.Frustum()
	local v = solo.Vector3(1, 2, 3)
	f:getNearPlane()
	f:getFarPlane()
	f:getLeftPlane()
	f:getRightPlane()
	f:getTopPlane()
	f:getBottomPlane()
	f:getMatrix()
	f:setMatrix(solo.Matrix())
	f:getCorners()
	f:getNearCorners()
	f:getFarCorners()
	f:intersectsPoint(v)
	f:intersectsRay(solo.Ray(v, v))
	f:intersectsBoundingBox(solo.BoundingBox(v, v))
	f:intersectsBoundingSphere(solo.BoundingSphere(v, 1))
	f:getPlaneIntersection(solo.Plane(v, 1))
end, "Frustum")
